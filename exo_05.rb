#{} string interpolation - permet de vérifier les informations inscrites et faire les calculs 
#output a string on the console
puts "On va compter le nombre d'heures de travail à THP"

#output a string and the result of the operation between the hash
puts "Travail :  #{10 * 5 * 11}"

#affiche le nombre de minutes
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"

#affiche la chaine de caractères qui est entre ""
puts "Et en secondes ?"

#affiche le résultat de l'opération
puts 10 * 5 * 11 * 60 * 60


